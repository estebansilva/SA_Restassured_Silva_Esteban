package Clase4.Ejercicio4;

public class Libro {

    String categoria;
    String autor;
    String titulo;
    int precio;

    public Libro(String unaCategoria, String unAutor, String unTitulo, int unPrecio){
        this.categoria = unaCategoria;
        this.autor = unAutor;
        this.titulo = unTitulo;
        this.precio = unPrecio;
    }
}
